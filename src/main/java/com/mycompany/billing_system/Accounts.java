    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        // TODO add your handling code here:
        String sNo = JOptionPane.showInputDialog(this,"Enter the serial number to delete");
        int num = Integer.parseInt(sNo);
      
        for(ArrayList<String> b : order){
            if(sNo.equals(b.get(0))){
                grandTotal -= Double.parseDouble(b.get(3));
                order.remove(num-1);
                break ;
            }
        }
        
        DefaultTableModel model = (DefaultTableModel)tItem.getModel();
        model.setRowCount(0);
        int c = 0 ;
        for(ArrayList<String> a : order){
            a.set(0,"" + ++c);
            model.addRow(new Object[]{a.get(0),a.get(1),a.get(2),a.get(3)});
        }
        count = c ;
        lGrandTotal.setText("Grand Total : " + grandTotal);
    }//GEN-LAST:event_bDeleteActionPerformed

