    static int count = 0 ;
    static double grandTotal = 0.0 ;
    static ArrayList<ArrayList<String>> order = new ArrayList<>();
    private void bAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAddItemActionPerformed
        // TODO add your handling code here:
        String item = cbItemList.getItemAt(cbItemList.getSelectedIndex());
        String quantity = cbQuantity.getItemAt(cbQuantity.getSelectedIndex());
        String price = Records.getPrice(item);
        Double total = Double.parseDouble(price)*Integer.parseInt(quantity);
        grandTotal += total ;
        
        ArrayList<String> a = new ArrayList<>();
        a.add(""+ ++count); // 0
        a.add(item); // 1
        a.add(quantity); // 2
        a.add("" +total); // 3
        a.add(price);  // 4
        order.add(a); 
        DefaultTableModel model = (DefaultTableModel)tItem.getModel();
        model.addRow(new Object[]{a.get(0),a.get(1),a.get(2),a.get(3)});
        
        lGrandTotal.setText("Grand Total : " + grandTotal);
    }//GEN-LAST:event_bAddItemActionPerformed

    private void bPrint1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPrint1ActionPerformed
        // TODO add your handling code here:
        String customer_name = tfCustomerName.getText();
        String customer_contact = tfCustomerContact.getText();
        
        int status = Records.storeOrder(customer_name , customer_contact , ""+grandTotal , cashier_name,order);
        int bill_no = Records.getBillNum();
        
        if(status == 1){
            JOptionPane.showMessageDialog(this , "Order completed");
            tfCustomerName.setText("");
            tfCustomerContact.setText("");
            DefaultTableModel model = (DefaultTableModel)tItem.getModel();
            model.setRowCount(0);
            lGrandTotal.setText("");
            Receipt r = new Receipt(bill_no ,""+ grandTotal, cashier_name ,customer_name ,customer_contact , order);
            r.setVisible(true);
            this.setVisible(false);
            order.clear();
            count = 0 ;
           
        }else{
            JOptionPane.showMessageDialog(this , "Something went wrong");
        }
        
    }//GEN-LAST:event_bPrint1ActionPerformed

